""" Mass conversion function between Kilogram,Tonne and Pound """
#functions

def kgtotonne(x):
    """Returns x converted to tonne"""
    return x*0.001
def tonnetokg(x):
    """Returns x converted to kilogram"""
    return x/0.001

def kgtopound(x):
    """Returns x converted to pound"""
    return x*2.20462

def poundtokg(x):
    """Returns x converted to kilogram"""
    return x/2.20462