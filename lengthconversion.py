#Functions

def miletokm(x):
    """ Function returned the value converted into kilometer"""
    return 1.609*x
def kmtomile(x):
    """ Function returned the value converted into kilometer"""
    return x/1.609
def feetintoinches(x):
    """ Function returned the value converted into inches"""
    return 12*x

def inchesintofeet(x):
    """ Function returned the value converted into feets"""
    return x/12
